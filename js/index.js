function changeColor(){
    let ccolor = document.getElementById("fundo");
    ccolor.style.background = "black";
    var textcolor = document.getElementById("text");
    textcolor.style.color = "white";
}


function outColor(){
    let ocolor = document.getElementById("fundo");
    ocolor.style.background = "white";
    var textcolor = document.getElementById("text");
    textcolor.style.color = "black";
}